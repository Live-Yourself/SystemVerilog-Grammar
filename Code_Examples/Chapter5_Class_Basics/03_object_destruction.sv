// =============================================================================
// 辅助类: Transaction
// =============================================================================
class Transaction;
  bit [31:0] addr;
  bit [31:0] data;
  bit        write;
  int        id;

  function new(bit [31:0] a, bit [31:0] d, bit w);
    static int counter = 0;
    counter = counter + 1;
    id    = counter;
    addr  = a;
    data  = d;
    write = w;
    $display("  [new] Transaction ID=%0d 已创建", id);
  endfunction

  function void display();
    $display("  Transaction[ID=%0d]: addr=0x%08h data=0x%08h %s",
             id, addr, data, write ? "WR" : "RD");
  endfunction
endclass

// =============================================================================
// 测试程序
// =============================================================================
module object_destruction_test;

  Transaction tr1;
  Transaction tr2;
  Transaction tr3;

  initial begin
    $display("========================================");
    $display(" 知识点3: 对象的释放与垃圾回收");
    $display("========================================\n");

    // =====================================================================
    // 示例1: 对象生命周期 -- 声明、构造、释放
    // =====================================================================
    $display("--- 示例1: 对象生命周期 ---");
    $display("[1] 声明: Transaction tr1;  → tr1==null ? %s",
             (tr1 == null) ? "YES" : "NO");

    tr1 = new(32'h1000_0000, 32'hDEAD_BEEF, 1);
    $display("[2] 构造: tr1 = new(...);    → tr1==null ? %s",
             (tr1 == null) ? "YES" : "NO");
    tr1.display();

    tr1 = null;
    $display("[3] 释放: tr1 = null;        → tr1==null ? %s  → 对象可被回收",
             (tr1 == null) ? "YES" : "NO");
    $display("");

    // =====================================================================
    // 示例2: 多句柄引用同一对象 -- 断一个不够, 全断才行
    // =====================================================================
    $display("--- 示例2: 多句柄引用 ---");
    tr1 = new(32'h2000_0000, 32'hCAFEBABE, 0);
    tr2 = tr1;
    $display("tr1 = new(...);  tr2 = tr1;  → 指向同一对象");
    tr1.display();

    tr1 = null;
    $display("tr1 = null;  → tr1==null:%s  tr2==null:%s  → 对象不回收",
             (tr1 == null) ? "YES" : "NO",
             (tr2 == null) ? "YES" : "NO");
    $display("  通过tr2仍能访问:");
    tr2.display();

    tr2 = null;
    $display("tr2 = null;  → 所有引用断开 → 对象可被回收");
    $display("");

    // =====================================================================
    // 示例3: 句柄重指向 -- 旧对象自动可回收
    // =====================================================================
    $display("--- 示例3: 句柄重指向 ---");
    tr1 = new(32'h3000_0000, 32'h11111111, 1);
    $display("tr1 = new(0x3000...); → addr=0x%08h", tr1.addr);

    tr1 = new(32'h4000_0000, 32'h22222222, 0);
    $display("tr1 = new(0x4000...); → addr=0x%08h  → 旧对象自动可回收",
             tr1.addr);
    $display("");

    // =====================================================================
    // 示例4: 三个句柄逐步释放
    // =====================================================================
    $display("--- 示例4: 三个句柄逐步释放 ---");
    tr1 = new(32'h5000_0000, 32'hAAAAAAAA, 1);
    tr2 = tr1;
    tr3 = tr1;
    $display("tr1=tr2=tr3指向同一对象  → 3个引用");

    tr1 = null;
    $display("tr1=null   → 2个引用  → 不回收");

    tr2 = null;
    $display("tr2=null   → 1个引用  → 不回收");

    tr3 = null;
    $display("tr3=null   → 0个引用  → 可被垃圾回收!");
    $display("");

    // =====================================================================
    // 示例5: 局部句柄的作用域
    // =====================================================================
    $display("--- 示例5: 局部句柄作用域 ---");
    begin
      Transaction local_tr;
      local_tr = new(32'h6000_0000, 32'hBBBBBBBB, 0);
      $display("块内: local_tr指向对象, ID=%0d", local_tr.id);
    end
    $display("块外: local_tr已离开作用域, 不可访问");
    $display("  (若该对象无其他引用,也可被回收)");
    $display("");

    // =====================================================================
    // 示例6: 验证实践中的内存管理
    // =====================================================================
    $display("--- 示例6: 验证实践中的内存管理 ---");
    $display("反面教材 (无清理, 内存持续增长):");
    $display("  forever begin t = new(); queue.push_back(t); end");
    $display("");
    $display("正确做法 (使用完毕后清理队列):");
    $display("  queue.push_back(t);  // 使用中");
    $display("  // ... 比对完成 ...");
    $display("  queue.delete();      // 释放所有引用, 对象可被回收");
    $display("");

    // =====================================================================
    $display("========================================");
    $display(" 关键要点:");
    $display("  1. 没有delete操作符, 靠自动垃圾回收");
    $display("  2. 所有句柄都不引用时, 对象才可被回收");
    $display("  3. tr = null 主动断开引用");
    $display("  4. tr = new(...) 重指向时旧对象自动可回收");
    $display("  5. 回收时机由仿真器决定, 程序员不可控");
    $display("  6. 队列/数组持有句柄会阻止回收, 需定期清理");
    $display("========================================");

    $finish;
  end

endmodule

