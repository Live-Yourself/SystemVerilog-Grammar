// =============================================================================
// 辅助类: Header (嵌套对象演示用)
// =============================================================================
class Header;
  bit [7:0] src_addr;
  bit [7:0] dst_addr;
  bit [7:0] pkt_type;

  function new(bit [7:0] src, bit [7:0] dst, bit [7:0] ptype);
    src_addr = src;
    dst_addr = dst;
    pkt_type = ptype;
  endfunction

  function void display();
    $display("    Header: src=0x%02h dst=0x%02h type=0x%02h",
             src_addr, dst_addr, pkt_type);
  endfunction
endclass

// =============================================================================
// 主类: Transaction
// =============================================================================
class Transaction;
  bit [31:0] addr;
  bit [31:0] data;
  bit        write;
  Header     hdr;  // 嵌套对象成员

  function new(bit [31:0] a, bit [31:0] d, bit w);
    addr  = a;
    data  = d;
    write = w;
  endfunction

  // 方法: 设置地址和数据
  function void set(bit [31:0] a, bit [31:0] d);
    addr = a;
    data = d;
  endfunction

  // 方法: 显示事务信息
  function void display();
    $display("    Transaction: addr=0x%08h data=0x%08h %s",
             addr, data, write ? "WR" : "RD");
  endfunction

  // 方法: 调用其他成员方法
  function void set_and_display(bit [31:0] a, bit [31:0] d);
    set(a, d);      // 内部调用同对象的set方法
    display();      // 内部调用同对象的display方法
  endfunction
endclass

// =============================================================================
// 测试程序
// =============================================================================
module member_access_test;

  Transaction tr1;
  Transaction tr2;
  bit [31:0]  saved_addr;
  bit [31:0]  saved_data;

  initial begin
    $display("========================================");
    $display(" 知识点4: 使用对象成员");
    $display("========================================\n");

    // =====================================================================
    // 示例1: 通过点号读取和写入成员变量
    // =====================================================================
    $display("--- 示例1: 读写成员变量 ---");
    tr1 = new(32'h0000_0000, 32'h0000_0000, 0);

    $display("初始值:");
    tr1.display();

    // 写入成员变量
    tr1.addr  = 32'h1000_0000;
    tr1.data  = 32'hDEAD_BEEF;
    tr1.write = 1;

    $display("修改后:");
    tr1.display();

    // 读取成员变量到局部变量
    saved_addr = tr1.addr;
    saved_data = tr1.data;
    $display("读取到局部变量: saved_addr=0x%08h, saved_data=0x%08h",
             saved_addr, saved_data);
    $display("");

    // =====================================================================
    // 示例2: 调用成员方法
    // =====================================================================
    $display("--- 示例2: 调用成员方法 ---");
    tr1.set(32'h2000_0000, 32'hCAFEBABE);  // 通过方法修改成员
    $display("调用set()后:");
    tr1.display();

    tr1.set_and_display(32'h3000_0000, 32'h12345678);  // 方法内调用其他方法
    $display("");

    // =====================================================================
    // 示例3: 方法内部直接访问成员(不需要句柄前缀)
    // =====================================================================
    $display("--- 示例3: 方法内部访问成员 ---");
    $display("set_and_display()方法内部实现:");
    $display("  function void set_and_display(bit [31:0] a, bit [31:0] d);");
    $display("    set(a, d);    // 直接调用同对象方法,无需this");
    $display("    display();    // 直接调用,无需tr1.display()");
    $display("  endfunction");
    $display("  → 方法内部用成员名直接访问,不需要句柄前缀\n");

    // =====================================================================
    // 示例4: 嵌套对象 -- 连续点号访问
    // =====================================================================
    $display("--- 示例4: 嵌套对象访问 ---");
    tr1 = new(32'h4000_0000, 32'hAAAAAAAA, 0);
    tr1.hdr = new(8'h10, 8'h20, 8'h01);  // 创建嵌套的Header对象

    $display("通过连续点号访问嵌套成员:");
    tr1.display();
    tr1.hdr.display();

    // 修改嵌套对象的成员
    tr1.hdr.src_addr = 8'hFF;
    tr1.hdr.dst_addr = 8'hEE;
    $display("修改嵌套成员后:");
    tr1.hdr.display();
    $display("");

    // =====================================================================
    // 示例5: null句柄访问 -- 运行时错误
    // =====================================================================
    $display("--- 示例5: null句柄访问 ---");
    tr2 = null;  // tr2未指向任何对象
    if (tr2 == null)
      $display("tr2 == null, 不能使用点号访问!");
    else begin
      tr2.display();  // 只有非null时才安全访问
    end
    $display("  → 对null句柄使用点号会导致运行时错误");
    $display("  → 最佳实践: 访问前先检查 (if (tr != null))\n");

    // =====================================================================
    // 示例6: 多句柄共享同一对象 -- 通过任一句柄修改,其他句柄可见
    // =====================================================================
    $display("--- 示例6: 多句柄共享对象的成员修改 ---");
    tr1 = new(32'h5000_0000, 32'h55555555, 1);
    tr2 = tr1;  // tr2和tr1指向同一对象

    $display("tr2 = tr1; 指向同一对象");
    tr1.display();

    $display("通过tr1修改成员:");
    tr1.addr = 32'h6000_0000;
    tr1.display();

    $display("通过tr2查看 (看到tr1的修改):");
    tr2.display();
    $display("  → 同一对象,任一句柄修改对所有句柄可见\n");

    // =====================================================================
    $display("========================================");
    $display(" 关键要点:");
    $display("  1. 点号(.)访问成员变量和方法");
    $display("  2. 方法内部直接用成员名,不需要句柄前缀");
    $display("  3. 嵌套对象通过连续点号访问");
    $display("  4. null句柄使用点号会导致运行时错误");
    $display("  5. 共享对象:任一句柄修改对所有句柄可见");
    $display("========================================");

    $finish;
  end

endmodule

