// =============================================================================
// 知识点讲解:
// 覆盖率分为两大类:
//   1. 代码覆盖率(Code Coverage): 仿真器自动收集,包括行覆盖率、翻转覆盖率、
//      条件覆盖率、表达式覆盖率等。它告诉你"哪些代码被执行过"。
//   2. 功能覆盖率(Functional Coverage): 验证工程师主动定义,描述设计规格中
//      "应该被验证的功能点是否都验证到了"。通过 covergroup 采样数据来衡量。
//
// 功能覆盖率的核心思想:
//   - 验证工程师根据设计规格定义"我关心什么"
//   - 在仿真过程中对关心的数据进行采样
//   - 仿真结束后查看覆盖率报告,判断验证是否充分
//   - 根据覆盖率缺口调整测试激励
// =============================================================================

//------------------------------------------------------------------------------
// 示例场景: 验证一个简单 ALU,我们关心所有操作码是否都被执行到
//------------------------------------------------------------------------------

// 定义 ALU 操作码枚举
typedef enum bit [2:0] {
  OPCODE_ADD  = 3'b000,
  OPCODE_SUB  = 3'b001,
  OPCODE_AND  = 3'b010,
  OPCODE_OR   = 3'b011,
  OPCODE_XOR  = 3'b100,
  OPCODE_NOP  = 3'b111
} opcode_e;

// ALU 结果事务
class AluTransaction;
  rand opcode_e opcode;
  rand bit [7:0] operand_a;
  rand bit [7:0] operand_b;

  function void display();
    $display("  opcode=%0s, a=0x%02h, b=0x%02h", opcode.name(), operand_a, operand_b);
  endfunction
endclass

//------------------------------------------------------------------------------
// 功能覆盖率: 定义我们关心的操作码覆盖情况
//------------------------------------------------------------------------------
covergroup OpCovGroup;
  // 覆盖点: 采样 opcode
  coverpoint opcode {
    // 每个枚举值一个 bin
    bins b_add  = {OPCODE_ADD};
    bins b_sub  = {OPCODE_SUB};
    bins b_and  = {OPCODE_AND};
    bins b_or   = {OPCODE_OR};
    bins b_xor  = {OPCODE_XOR};
    bins b_nop  = {OPCODE_NOP};
  }
endgroup

//==============================================================================
// 测试模块
//==============================================================================
module coverage_overview_test;

  AluTransaction txn;       // 事务对象
  OpCovGroup     op_cov;    // 覆盖组实例

  initial begin
    txn    = new();
    op_cov = new();

    $display("========================================");
    $display("KP01: 覆盖率概述 - 功能覆盖率 vs 代码覆盖率");
    $display("========================================\n");

    //------------------------------------------------------------------
    // 1. 模拟第一轮激励: 只发了 ADD 和 SUB
    //------------------------------------------------------------------
    $display("--- 第1轮激励 (只覆盖部分操作码) ---");
    repeat (10) begin
      // 限制 opcode 范围,只产生 ADD / SUB
      if (!txn.randomize() with { opcode inside {OPCODE_ADD, OPCODE_SUB}; })
        $fatal("随机化失败");
      op_cov.sample();   // 采样覆盖率
      txn.display();
    end

    $display("\n  [覆盖率状态] 请观察: b_and / b_or / b_xor / b_nop 未被命中");
    $display("  这就是'覆盖率缺口'——说明验证还不充分\n");

    //------------------------------------------------------------------
    // 2. 模拟第二轮激励: 补充覆盖剩余操作码
    //------------------------------------------------------------------
    $display("--- 第2轮激励 (覆盖所有操作码) ---");
    repeat (10) begin
      if (!txn.randomize())
        $fatal("随机化失败");
      op_cov.sample();
      txn.display();
    end

    $display("\n========================================");
    $display("关键要点:");
    $display("  1. 代码覆盖率: 仿真器自动收集,关注'代码是否被执行'");
    $display("  2. 功能覆盖率: 验证工程师定义,关注'规格功能是否被验证'");
    $display("  3. 功能覆盖率通过 covergroup + coverpoint 实现");
    $display("  4. 通过 sample() 对数据进行采样");
    $display("  5. 覆盖率报告帮助发现'验证缺口',指导激励生成");
    $display("========================================\n");

    // 仿真结束
    $display("--- 仿真器覆盖率报告 ---");
    $display("  (请使用仿真器命令查看: QuestaSim -> coverage report)");
  end

endmodule

